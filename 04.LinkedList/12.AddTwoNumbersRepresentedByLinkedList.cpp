class Solution
{
    public:
    Node* reverse(Node *head)
    {
        // Initialize current, previous and
        // next pointers
        Node* current = head;
        Node *prev = NULL, *next = NULL;
 
        while (current != NULL) {
            // Store next
            next = current->next;
 
            // Reverse current node's pointer
            current->next = prev;
 
            // Move pointers one position ahead.
            prev = current;
            current = next;
        }
        head = prev;
        return head;
    }
    //Function to add two numbers represented by linked list.
    struct Node* addTwoLists(struct Node* first, struct Node* second)
    {
        // code here
        first=reverse(first);
        second=reverse(second);
        // cout<<"f"<<first->next->data<<"s"<<second->data;
        Node *res=NULL;
        Node *tmp,*prev=NULL;
        int c=0,sum;
        while(first!=NULL||second!=NULL){
            sum=c+(first?first->data:0)+(second?second->data:0);
            c=sum/10;
            sum%=10;
            tmp=new Node(sum);
            if(res!=NULL){
                prev->next=tmp;
            }else{
                res=tmp;
            }
            prev=tmp;
            if(first)
            first=first->next;
            if(second)
            second=second->next;
        }
        if(c)
        tmp->next= new Node(c);
        res=reverse(res);
        return res;
    }
};


